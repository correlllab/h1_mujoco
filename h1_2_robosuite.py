class _MagpieGripperBase(GripperModel):
    """Shared Magpie 4-bar parallel-jaw gripper behaviour.

    Two independently-actuated crank joints (finger_joint1/2) close the jaws via
    the in-XML <equality><connect> 4-bar linkage. We never call env.step(), so
    format_action is the identity and the controller is construction-only.
    """

    def format_action(self, action):
        return action

    @property
    def init_qpos(self):
        # Length == number of gripper joints in the MJCF (3 hinges x 2 fingers).
        return np.zeros(6)

    @property
    def speed(self):
        return 0.2

    @property
    def dof(self):
        return 2

    @property
    def _important_geoms(self):
        return {
            "left_finger": ["left_pad1", "left_pad2", "left_pad3", "left_pad4"],
            "right_finger": ["right_pad1", "right_pad2", "right_pad3", "right_pad4"],
            "left_fingerpad": ["left_pad4"],
            "right_fingerpad": ["right_pad4"],
        }


@register_gripper
class MagpieRightGripper(_MagpieGripperBase):
    def __init__(self, idn=0):
        super().__init__(GRIPPER_RIGHT_XML, idn=idn)


@register_gripper
class MagpieLeftGripper(_MagpieGripperBase):
    def __init__(self, idn=0):
        super().__init__(GRIPPER_LEFT_XML, idn=idn)


