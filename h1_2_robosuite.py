"""Register the Unitree H1-2 (+ Magpie hands) as a first-class robosuite robot.

Importing this module once (before ``robocasa.utils.env_utils.create_env`` /
``robosuite.make``) makes ``robots="H1_2"`` resolvable and installs the RoboCasa
placement patch so the full-legged humanoid is positioned by its pelvis free
joint (RoboCasa's stock path writes mobile-base joints that this robot lacks).

Assets live in ``CL_Assets/robosuite_assets`` (generated by
``CL_Assets/robosuite_assets/build_assets.py``). The robot keeps all 27 actuators
and its floating base; locomotion is driven externally by the ROS walking policy
over DDS — robosuite controllers are never stepped (we own ``mujoco.mj_step``).

NOTE: robosuite/robocasa import only inside the MuJoCo container (mujoco==3.3.1).
This module is syntax-valid on the host but must be exercised in the container.
The API here follows robosuite master / robocasa main as surveyed in the design
doc (docs/superpowers/specs/2026-06-08-robocasa-mujoco-eval-integration-design.md).
"""
